/* 
--> Data Types
     Defines what type of data will be stored in the variable
*/
public class datatypes {
    public static void main(String[] args){
        int a = 10;           // integer variable
        double b = 3.14;      // double variable
        char c = 'A';         // character variable
        String d = "Hello";   // string variable and string must be in double quotes("")
        boolean e = true;     // boolean variable
        long f = 100000L;     // long variable
        short g = 100;        // short variable
        byte h = 127;         // byte variable

        System.out.println("Integer: " + a);
        System.out.println("Double: " + b);
        System.out.println("Character: " + c);
        System.out.println("String: " + d);
        System.out.println("Boolean: " + e);
        System.out.println("Long: " + f);
        System.out.println("Short: " + g);
    }   
}
 /* INTEGER: 4 bytes (32 bits) - Whole numbers
    DOUBLE: 8 bytes (64 bits) - Decimal numbers
    CHAR: 2 bytes (16 bits) - Single character
    STRING: Variable length - Sequence of characters
    BOOLEAN: 1 bit - true or false
    LONG: 8 bytes (64 bits) - Whole numbers
    SHORT: 2 bytes (16 bits) - Whole numbers
    BYTE: 1 byte (8 bits) - Whole numbers
  */