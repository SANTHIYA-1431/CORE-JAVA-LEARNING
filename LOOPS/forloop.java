/*  for loop :
   - The for loop is used when we know the number of iterations(how many times to repeat the task)
   - The for statement includes the initialization, condition, and increment/decrement in one line. 
    Syntax:
    for(initialization ; condition ; increment (++)\ decrement(--)){
         //statement or code to be executed
    }
 */
public class forloop {
    public static void main(String[] args) {
        for(int i=0 ; i < 11 ;i++){ // this prints 0 to 10 
            System.out.println(i);
        }
    }
}