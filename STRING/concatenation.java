/*Concatenation is the process of combining two or more strings (or values) 
into a single string using the + operator*/
class concatenation{
    public static void main(String[] args) {
        String a1="java-";
        String a2="virtual-";
        String a3=a1+a2;
        System.out.println(a3);
        String a4="machine";
        String a5=a3+a4;
        System.out.println(a5);
    }
}